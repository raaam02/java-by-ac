public class MathClass {
    public static void main(String[] args) {
        System.err.println("Minimum " + Math.min(15, 20));
        System.out.println("Maximum " + Math.max(30, 40));
        System.out.println("SQRT " + Math.sqrt(30));
        System.out.println("Power " + Math.pow(2, 10));
        System.out.println("Absolute " + Math.abs(-150.5924));
    }
}